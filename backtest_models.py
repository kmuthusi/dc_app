def _save_dataframe(df: pd.DataFrame, out_path: Path) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    suf = out_path.suffix.lower()
    if suf in [".parquet", ".pq"]:
        try:
            df.to_parquet(out_path, index=False)
        except Exception as e:
            fallback = out_path.with_suffix(".csv")
            df.to_csv(fallback, index=False)
            print(f"[warn] Failed to write parquet ({e}). Wrote CSV instead: {fallback}")
    else:
        df.to_csv(out_path, index=False)


def main() -> None:
    args = parse_args()

    # Resolve inputs
    if args.all_euro_files:
        all_euro_paths = [Path(p) for p in args.all_euro_files]
    else:
        all_euro_paths = _discover_all_euro_files(args.all_euro_dir)
    new_leagues_path = Path(args.new_leagues_file) if args.new_leagues_file else None

    if not all_euro_paths and new_leagues_path is None:
        raise SystemExit("No inputs provided. Use --all-euro-dir/--all-euro-files and/or --new-leagues-file")

    master = build_master_dataset(
        all_euro_paths=all_euro_paths,
        new_leagues_path=new_leagues_path,
        last_n_seasons=args.last_n_seasons,
    )
    if master.empty:
        raise SystemExit("Master dataset is empty after ingestion.")

    if args.processed_out:
        _save_dataframe(master, Path(args.processed_out))

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    half_life_grid = _parse_float_grid(args.half_life_days_grid)
    xg_grid = _parse_float_grid(args.xg_weight_grid)
    draw_threshold_grid = _parse_float_grid(args.draw_threshold_grid)
    if not half_life_grid:
        half_life_grid = [180.0]
    if not xg_grid:
        xg_grid = [0.0]
    if not draw_threshold_grid:
        draw_threshold_grid = [float(args.draw_threshold)]

    # Filter leagues
    if args.league_id:
        leagues = [str(args.league_id)]
    else:
        leagues = sorted(master["league_id"].dropna().astype(str).unique().tolist())

    summaries: List[Dict] = []
    best_params: Dict[str, Dict] = {}
    best_exp_thresholds: Dict[str, Dict] = {}

    for league_id in leagues:
        g = master[master["league_id"].astype(str) == str(league_id)].copy()
        g = g.dropna(subset=["datetime", "home_team", "away_team", "home_goals", "away_goals"]).copy()
        if g.empty:
            print(f"[warn] {league_id}: no played matches with datetime")
            continue

        league_dir = out_dir / str(league_id)
        league_dir.mkdir(parents=True, exist_ok=True)

        # Keep deterministic ordering
        g = g.sort_values("datetime").reset_index(drop=True)

        best = None
        best_exp = None
        for half_life in half_life_grid:
            for xg_w in xg_grid:
                for draw_threshold in draw_threshold_grid:
                    pred_df, summary = rolling_backtest(
                        g,
                        half_life_days=half_life,
                        xg_weight=xg_w,
                        draw_threshold=draw_threshold,
                        max_goals=args.max_goals,
                        l2=args.l2,
                        retrain_every=args.retrain_every,
                        min_train_matches=args.min_train_matches,
                        eval_season=args.eval_season,
                        eval_start_date=args.eval_start_date,
                    )
                    summary["league_id"] = str(league_id)
                    summaries.append(summary)

                    tag = f"hl{half_life:g}_xg{xg_w:g}_dt{draw_threshold:g}".replace(".", "p")
                    pred_path = league_dir / f"{tag}_predictions.csv"
                    pred_df.to_csv(pred_path, index=False)

                    # Calibration on top_prob
                    cal_df = pred_df.copy()
                    cal_df["model_correct"] = pd.to_numeric(cal_df["model_correct"], errors="coerce")
                    cal = _calibration_table(cal_df, prob_col="top_prob", correct_col="model_correct", n_bins=args.calibration_bins)
                    cal.to_csv(league_dir / f"{tag}_calibration.csv", index=False)

                    # Track best by model_logloss (lower is better)
                    ll = summary.get("model_logloss")
                    if ll is not None and np.isfinite(ll):
                        if best is None or float(ll) < float(best["model_logloss"]):
                            best = dict(summary)

                    # Track best expected-goals threshold by exp_accuracy (higher is better).
                    ea = summary.get("exp_accuracy")
                    if ea is not None and np.isfinite(ea):
                        if best_exp is None:
                            best_exp = dict(summary)
                        else:
                            better_acc = float(ea) > float(best_exp["exp_accuracy"])
                            tie_better_ll = float(ea) == float(best_exp["exp_accuracy"]) and float(summary.get("model_logloss", np.inf)) < float(best_exp.get("model_logloss", np.inf))
                            tie_smaller_threshold = float(ea) == float(best_exp["exp_accuracy"]) and float(summary.get("model_logloss", np.inf)) == float(best_exp.get("model_logloss", np.inf)) and float(draw_threshold) < float(best_exp.get("draw_threshold", np.inf))
                            if better_acc or tie_better_ll or tie_smaller_threshold:
                                best_exp = dict(summary)

                    print(
                        f"[ok] {league_id} {tag}: n_pred={summary.get('n_pred')}, "
                        f"model_logloss={summary.get('model_logloss')}, exp_accuracy={summary.get('exp_accuracy')}, "
                        f"book_logloss={summary.get('book_logloss')}"
                    )

        if best is not None:
            best_params[str(league_id)] = best
            (league_dir / "best_params.json").write_text(json.dumps(best, indent=2), encoding="utf-8")
        if best_exp is not None:
            best_exp_thresholds[str(league_id)] = best_exp
            (league_dir / "best_exp_threshold.json").write_text(json.dumps(best_exp, indent=2), encoding="utf-8")

    # Write overall summaries
    summary_df = pd.DataFrame(summaries)
    summary_df.to_csv(out_dir / "summary.csv", index=False)
    (out_dir / "summary.json").write_text(json.dumps(summaries, indent=2), encoding="utf-8")
    (out_dir / "best_params_all.json").write_text(json.dumps(best_params, indent=2), encoding="utf-8")
    (out_dir / "best_exp_thresholds_all.json").write_text(json.dumps(best_exp_thresholds, indent=2), encoding="utf-8")


if __name__ == "__main__":
    main()
