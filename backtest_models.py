EPS = 1e-15


def _expected_goal_outcome(exp_home_goals: float, exp_away_goals: float, draw_threshold: float) -> str:
    diff = float(exp_home_goals) - float(exp_away_goals)
    if abs(diff) <= float(draw_threshold):
        return "D"
    return "H" if diff > 0 else "A"


def _season_start_year(season: str) -> Optional[int]:
    if season is None or (isinstance(season, float) and np.isnan(season)):
        return None
    m = re.search(r"(\d{4})", str(season))
    return int(m.group(1)) if m else None


def _parse_float_grid(s: str) -> List[float]:
    """Parse comma-separated floats (e.g. '0,90,180')."""
    if s is None:
        return []
    parts = [p.strip() for p in str(s).split(",") if p.strip() != ""]
    return [float(p) for p in parts]


def _discover_all_euro_files(all_euro_dir: Optional[str]) -> List[Path]:
    if not all_euro_dir:
        return []
    d = Path(all_euro_dir)
    if not d.exists():
        raise FileNotFoundError(f"all-euro-dir not found: {d}")

    files = sorted(d.glob("all-euro-data-*.xlsx"))
    if not files:
        files = sorted(d.glob("*.xlsx"))
    return list(files)


def _true_outcome(home_goals: float, away_goals: float) -> str:
    if home_goals > away_goals:
        return "H"
    if home_goals < away_goals:
        return "A"
    return "D"


def _multiclass_brier(p: np.ndarray, y: np.ndarray) -> float:
    """Multi-class Brier score for one observation.

    p: shape (3,) probabilities in H,D,A order
    y: shape (3,) one-hot
    """
    return float(np.sum((p - y) ** 2))


def _odds_to_probs(odds_h: float, odds_d: float, odds_a: float) -> Optional[Tuple[float, float, float]]:
    """Convert 1X2 odds to implied probabilities and remove overround."""
    try:
        if not (np.isfinite(odds_h) and np.isfinite(odds_d) and np.isfinite(odds_a)):
            return None
        if odds_h <= 1e-9 or odds_d <= 1e-9 or odds_a <= 1e-9:
            return None
        inv = np.array([1.0 / odds_h, 1.0 / odds_d, 1.0 / odds_a], dtype=float)
        s = float(inv.sum())
        if s <= 0:
            return None
        p = inv / s
        return float(p[0]), float(p[1]), float(p[2])
    except Exception:
        return None


def _calibration_table(df: pd.DataFrame, prob_col: str, correct_col: str, n_bins: int = 10) -> pd.DataFrame:
    """Calibration of a scalar probability vs empirical accuracy.

    We bin `prob_col` into n_bins equal-width bins on [0,1] and compute:
      - count
      - mean_pred_prob
      - empirical_accuracy
    """
    x = df[[prob_col, correct_col]].dropna().copy()
    if x.empty:
        return pd.DataFrame(columns=["bin", "count", "mean_pred_prob", "empirical_accuracy"]) 

    x["bin"] = pd.cut(x[prob_col], bins=np.linspace(0, 1, n_bins + 1), include_lowest=True)
    out = (
        x.groupby("bin", observed=False)
        .agg(
            count=(prob_col, "size"),
            mean_pred_prob=(prob_col, "mean"),
            empirical_accuracy=(correct_col, "mean"),
        )
        .reset_index()
    )
    return out


def _select_eval_matches(
    played: pd.DataFrame,
    eval_season: Optional[str] = None,
    eval_start_date: Optional[str] = None,
) -> Tuple[pd.DataFrame, str]:
    """Choose evaluation matches.

    Priority:
      1) eval_start_date (YYYY-MM-DD)
      2) eval_season
      3) most recent season in data
    """
    if eval_start_date:
        start = pd.to_datetime(eval_start_date, errors="coerce")
        if pd.isna(start):
            raise ValueError(f"Could not parse eval_start_date: {eval_start_date!r}")
        ev = played[played["datetime"] >= start].copy()
        label = f"from_{start.date().isoformat()}"
        return ev, label

    if eval_season:
        ev = played[played["season"].astype(str) == str(eval_season)].copy()
        label = f"season_{eval_season}"
        return ev, label

    # most recent season by extracted start-year
    seasons = played["season"].dropna().astype(str).unique().tolist()
    if not seasons:
        return played.iloc[0:0].copy(), "no_season"
    seasons_sorted = sorted(seasons, key=lambda s: (_season_start_year(s) or -10**9, s))
    last = seasons_sorted[-1]
    ev = played[played["season"].astype(str) == str(last)].copy()
    label = f"season_{last}"
    return ev, label


def rolling_backtest(
    played: pd.DataFrame,
    *,
    half_life_days: float,
    xg_weight: float,
    draw_threshold: float,
    max_goals: int,
    l2: float,
    retrain_every: int,
    min_train_matches: int,
    eval_season: Optional[str],
    eval_start_date: Optional[str],
) -> Tuple[pd.DataFrame, Dict]:
    """Run a rolling backtest on a single league dataframe."""

    played = played.sort_values("datetime").reset_index(drop=True)

    eval_df, eval_label = _select_eval_matches(played, eval_season=eval_season, eval_start_date=eval_start_date)
    eval_df = eval_df.sort_values("datetime").reset_index(drop=True)
    if eval_df.empty:
        return eval_df, {
            "eval_label": eval_label,
            "n_eval": 0,
            "n_pred": 0,
            "error": "empty evaluation set",
        }

    # Decay rate
    if half_life_days and half_life_days > 0:
        xi = log(2.0) / float(half_life_days)
    else:
        xi = 0.0

    preds: List[Dict] = []

    # Rolling: retrain at chunk boundaries based on eval index
    n_eval = len(eval_df)
    for start in range(0, n_eval, max(1, retrain_every)):
        chunk = eval_df.iloc[start : start + max(1, retrain_every)].copy()
        as_of = pd.to_datetime(chunk["datetime"].iloc[0])

        train = played[played["datetime"] < as_of].copy()
        if len(train) < min_train_matches:
            # Not enough history yet; skip these matches
            for _, r in chunk.iterrows():
                preds.append(
                    {
                        "datetime": r["datetime"],
                        "season": r.get("season"),
                        "home_team": r["home_team"],
                        "away_team": r["away_team"],
                        "home_goals": r["home_goals"],
                        "away_goals": r["away_goals"],
                        "true_outcome": _true_outcome(r["home_goals"], r["away_goals"]),
                        "exp_home_goals": np.nan,
                        "exp_away_goals": np.nan,
                        "exp_outcome": np.nan,
                        "exp_correct": np.nan,
                        "p_home": np.nan,
                        "p_draw": np.nan,
                        "p_away": np.nan,
                        "pred_outcome": np.nan,
                        "top_prob": np.nan,
                        "model_logloss": np.nan,
                        "model_brier": np.nan,
                        "model_correct": np.nan,
                        "book_p_home": np.nan,
                        "book_p_draw": np.nan,
                        "book_p_away": np.nan,
                        "book_logloss": np.nan,
                        "book_brier": np.nan,
                        "book_correct": np.nan,
                        "note": f"skipped_not_enough_train({len(train)}<{min_train_matches})",
                    }
                )
            continue

        model = DixonColesModel(xi=xi, max_goals=max_goals, l2=l2, xg_weight=float(xg_weight))

        try:
            model.fit(train)
        except Exception as e:
            # If fit fails, mark predictions as missing for this chunk
            for _, r in chunk.iterrows():
                preds.append(
                    {
                        "datetime": r["datetime"],
                        "season": r.get("season"),
                        "home_team": r["home_team"],
                        "away_team": r["away_team"],
                        "home_goals": r["home_goals"],
                        "away_goals": r["away_goals"],
                        "true_outcome": _true_outcome(r["home_goals"], r["away_goals"]),
                        "exp_home_goals": np.nan,
                        "exp_away_goals": np.nan,
                        "exp_outcome": np.nan,
                        "exp_correct": np.nan,
                        "p_home": np.nan,
                        "p_draw": np.nan,
                        "p_away": np.nan,
                        "pred_outcome": np.nan,
                        "top_prob": np.nan,
                        "model_logloss": np.nan,
                        "model_brier": np.nan,
                        "model_correct": np.nan,
                        "book_p_home": np.nan,
                        "book_p_draw": np.nan,
                        "book_p_away": np.nan,
                        "book_logloss": np.nan,
                        "book_brier": np.nan,
                        "book_correct": np.nan,
                        "note": f"fit_failed: {e}",
                    }
                )
            continue

        for _, r in chunk.iterrows():
            ht = r["home_team"]
            at = r["away_team"]
            hg = float(r["home_goals"])
            ag = float(r["away_goals"])
            true = _true_outcome(hg, ag)

            # bookmaker probs (if odds exist)
            odds_h = r.get("odds_home", np.nan)
            odds_d = r.get("odds_draw", np.nan)
            odds_a = r.get("odds_away", np.nan)
            book = _odds_to_probs(odds_h, odds_d, odds_a)

            try:
                lam, nu = model.expected_goals(ht, at)
                exp_out = _expected_goal_outcome(lam, nu, draw_threshold)
                probs = model.outcome_probs(ht, at)
                p = np.array([probs["H"], probs["D"], probs["A"]], dtype=float)
                p = np.clip(p, EPS, 1.0)
                p = p / p.sum()

                pred_idx = int(np.argmax(p))
                pred_out = ["H", "D", "A"][pred_idx]
                top_prob = float(np.max(p))

                y = np.array([1.0 if true == "H" else 0.0, 1.0 if true == "D" else 0.0, 1.0 if true == "A" else 0.0])
                model_logloss = -float(np.log(p[["H", "D", "A"].index(true)]))
                model_brier = _multiclass_brier(p, y)
                model_correct = float(pred_out == true)
                exp_correct = float(exp_out == true)

            except KeyError as e:
                lam, nu = np.nan, np.nan
                exp_out = np.nan
                p = np.array([np.nan, np.nan, np.nan], dtype=float)
                pred_out = np.nan
                top_prob = np.nan
                model_logloss = np.nan
                model_brier = np.nan
                model_correct = np.nan
                exp_correct = np.nan
                note = f"unknown_team: {e}"
            except Exception as e:
                lam, nu = np.nan, np.nan
                exp_out = np.nan
                p = np.array([np.nan, np.nan, np.nan], dtype=float)
                pred_out = np.nan
                top_prob = np.nan
                model_logloss = np.nan
                model_brier = np.nan
                model_correct = np.nan
                exp_correct = np.nan
                note = f"pred_failed: {e}"
            else:
                note = ""

            # Bookmaker metrics
            if book is not None:
                bp = np.array(book, dtype=float)
                bp = np.clip(bp, EPS, 1.0)
                bp = bp / bp.sum()
                bpred_out = ["H", "D", "A"][int(np.argmax(bp))]
                by = np.array([1.0 if true == "H" else 0.0, 1.0 if true == "D" else 0.0, 1.0 if true == "A" else 0.0])
                blogloss = -float(np.log(bp[["H", "D", "A"].index(true)]))
                bbrier = _multiclass_brier(bp, by)
                bcorrect = float(bpred_out == true)
                book_ph, book_pd, book_pa = float(bp[0]), float(bp[1]), float(bp[2])
            else:
                book_ph = book_pd = book_pa = np.nan
                blogloss = bbrier = bcorrect = np.nan

            preds.append(
                {
                    "datetime": r["datetime"],
                    "season": r.get("season"),
                    "home_team": ht,
                    "away_team": at,
                    "home_goals": hg,
                    "away_goals": ag,
                    "true_outcome": true,
                    "exp_home_goals": lam,
                    "exp_away_goals": nu,
                    "exp_outcome": exp_out,
                    "exp_correct": exp_correct,
                    "p_home": float(p[0]) if np.isfinite(p[0]) else np.nan,
                    "p_draw": float(p[1]) if np.isfinite(p[1]) else np.nan,
                    "p_away": float(p[2]) if np.isfinite(p[2]) else np.nan,
                    "pred_outcome": pred_out,
                    "top_prob": top_prob,
                    "model_logloss": model_logloss,
                    "model_brier": model_brier,
                    "model_correct": model_correct,
                    "book_p_home": book_ph,
                    "book_p_draw": book_pd,
                    "book_p_away": book_pa,
                    "book_logloss": blogloss,
                    "book_brier": bbrier,
                    "book_correct": bcorrect,
                    "note": note,
                }
            )

    pred_df = pd.DataFrame(preds)
    n_pred = int(pred_df["model_logloss"].notna().sum())

    # Summary metrics (model)
    model_mask = pred_df["model_logloss"].notna()
    if model_mask.any():
        model_logloss = float(pred_df.loc[model_mask, "model_logloss"].mean())
        model_brier = float(pred_df.loc[model_mask, "model_brier"].mean())
        model_acc = float(pred_df.loc[model_mask, "model_correct"].mean())
        exp_acc = float(pred_df.loc[model_mask, "exp_correct"].mean())
    else:
        model_logloss = model_brier = model_acc = exp_acc = float("nan")

    # Summary metrics (bookmaker)
    book_mask = pred_df["book_logloss"].notna()
    if book_mask.any():
        book_logloss = float(pred_df.loc[book_mask, "book_logloss"].mean())
        book_brier = float(pred_df.loc[book_mask, "book_brier"].mean())
        book_acc = float(pred_df.loc[book_mask, "book_correct"].mean())
    else:
        book_logloss = book_brier = book_acc = float("nan")

    summary = {
        "eval_label": eval_label,
        "n_eval": int(len(eval_df)),
        "n_pred": n_pred,
        "half_life_days": float(half_life_days),
        "xi": float(xi),
        "xg_weight": float(xg_weight),
        "draw_threshold": float(draw_threshold),
        "max_goals": int(max_goals),
        "l2": float(l2),
        "retrain_every": int(retrain_every),
        "min_train_matches": int(min_train_matches),
        "model_logloss": model_logloss,
        "model_brier": model_brier,
        "model_accuracy": model_acc,
        "exp_accuracy": exp_acc,
        "book_logloss": book_logloss,
        "book_brier": book_brier,
        "book_accuracy": book_acc,
    }
    return pred_df, summary


