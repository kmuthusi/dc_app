def build_master_dataset(
    all_euro_paths: Iterable[Path],
    new_leagues_path: Optional[Path] = None,
    last_n_seasons: int = 3,
) -> pd.DataFrame:
    """Load all sources, standardize columns, keep last N seasons per league, and concatenate."""

    frames: List[pd.DataFrame] = []

    all_euro_paths = list(all_euro_paths)
    if all_euro_paths:
        frames.append(load_all_euro_workbooks(all_euro_paths))

    if new_leagues_path is not None:
        frames.append(load_new_leagues_workbook(new_leagues_path))

    if not frames:
        return pd.DataFrame()

    df = pd.concat(frames, ignore_index=True)

    # Canonical ordering / ensure columns exist
    for col in [
        "source",
        "league_id",
        "country",
        "league_name",
        "season",
        "date",
        "time",
        "datetime",
        "home_team",
        "away_team",
        "home_goals",
        "away_goals",
        "home_xg",
        "away_xg",
        "result",
        "odds_home",
        "odds_draw",
        "odds_away",
    ]:
        if col not in df.columns:
            df[col] = np.nan

    # Keep last N seasons per league
    if last_n_seasons is not None and last_n_seasons > 0:
        df = keep_last_n_seasons(df, "season", n=last_n_seasons)

    # Coerce goal/odds numeric
    df["home_goals"] = pd.to_numeric(df["home_goals"], errors="coerce")
    df["away_goals"] = pd.to_numeric(df["away_goals"], errors="coerce")
    df["home_xg"] = pd.to_numeric(df["home_xg"], errors="coerce")
    df["away_xg"] = pd.to_numeric(df["away_xg"], errors="coerce")
    df["odds_home"] = pd.to_numeric(df["odds_home"], errors="coerce")
    df["odds_draw"] = pd.to_numeric(df["odds_draw"], errors="coerce")
    df["odds_away"] = pd.to_numeric(df["odds_away"], errors="coerce")

    # Standardize result values (H/D/A)
    if "result" in df.columns:
        df["result"] = df["result"].astype(str).str.strip().str.upper()
        df.loc[~df["result"].isin(["H", "D", "A"]), "result"] = np.nan

    # Some safety cleanup
    df["home_team"] = df["home_team"].astype(str).str.strip()
    df["away_team"] = df["away_team"].astype(str).str.strip()

    # Remove obviously invalid matches
    df = df[(df["home_team"] != "") & (df["away_team"] != "")]
    df = df[df["home_team"].notna() & df["away_team"].notna()]

    # Sort
    df = df.sort_values(["league_id", "datetime"], na_position="last").reset_index(drop=True)

    return df
